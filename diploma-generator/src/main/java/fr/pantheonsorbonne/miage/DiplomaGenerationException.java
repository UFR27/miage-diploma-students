package fr.pantheonsorbonne.miage;

public class DiplomaGenerationException extends Exception {

	public DiplomaGenerationException() {
		super();
	}

	public DiplomaGenerationException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public DiplomaGenerationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DiplomaGenerationException(String arg0) {
		super(arg0);
	}

	public DiplomaGenerationException(Throwable arg0) {
		super(arg0);
	}

}
