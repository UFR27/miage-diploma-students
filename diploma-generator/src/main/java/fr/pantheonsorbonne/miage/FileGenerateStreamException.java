package fr.pantheonsorbonne.miage;

public class FileGenerateStreamException extends RuntimeException {

	public FileGenerateStreamException() {

	}

	public FileGenerateStreamException(String message) {
		super(message);

	}

	public FileGenerateStreamException(Throwable cause) {
		super(cause);

	}

	public FileGenerateStreamException(String message, Throwable cause) {
		super(message, cause);

	}

	public FileGenerateStreamException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
