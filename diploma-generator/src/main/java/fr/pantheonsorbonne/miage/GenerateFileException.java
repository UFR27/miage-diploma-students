package fr.pantheonsorbonne.miage;

public class GenerateFileException extends Exception {

	public GenerateFileException() {

	}

	public GenerateFileException(String message) {
		super(message);

	}

	public GenerateFileException(Throwable cause) {
		super(cause);

	}

	public GenerateFileException(String message, Throwable cause) {
		super(message, cause);

	}

	public GenerateFileException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
