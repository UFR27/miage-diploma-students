package fr.pantheonsorbonne.miage;

public class GetContentException extends Exception {

	public GetContentException() {

	}

	public GetContentException(String message) {
		super(message);

	}

	public GetContentException(Throwable cause) {
		super(cause);

	}

	public GetContentException(String message, Throwable cause) {
		super(message, cause);

	}

	public GetContentException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
