package fr.pantheonsorbonne.miage;

public class FileGenerateException extends RuntimeException {

	public FileGenerateException() {

	}

	public FileGenerateException(String arg0) {
		super(arg0);

	}

	public FileGenerateException(Throwable cause) {
		super(cause);

	}

	public FileGenerateException(String message, Throwable cause) {
		super(message, cause);

	}

	public FileGenerateException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
