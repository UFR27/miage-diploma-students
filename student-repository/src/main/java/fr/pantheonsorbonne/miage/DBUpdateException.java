package fr.pantheonsorbonne.miage;

public class DBUpdateException extends RuntimeException {

	public DBUpdateException() {
	
	}

	public DBUpdateException(String arg0) {
		super(arg0);
		
	}

	public DBUpdateException(Throwable arg0) {
		super(arg0);
		
	}

	public DBUpdateException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public DBUpdateException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

}
